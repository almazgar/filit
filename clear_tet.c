#include "fillit.h"

char	**clear_tet(char **map, int tet, int size)
{
	int x;
	int y;

	x = -1;
	while (++x < size)
	{
		y = -1;
		while (++y < size)
		{
			if (map[x][y] == tet)
				map[x][y] = '.';
		}
	}
	return (map);
}
