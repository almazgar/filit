#include "fillit.h"

int			ft_x(char **sq)
{
	int		x;
	int		y;

	x = -1;
	while (++x < 4)
	{
		y = -1;
		while (++y < 4)
			if (sq[x][y] == ft_tet_char(sq))
				return (x);
	}
	return (0);
}
