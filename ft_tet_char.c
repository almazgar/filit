#include "fillit.h"

int			ft_tet_char(char **sq)
{
	int		x;
	int		y;

	x = -1;
	while (++x < 4)
	{
		y = -1;
		while (++y < 4)
			if (ft_isalpha(sq[x][y]))
				return (sq[x][y]);
	}
	return (0);
}
